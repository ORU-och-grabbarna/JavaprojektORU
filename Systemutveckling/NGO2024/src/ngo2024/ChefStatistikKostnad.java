    /**
 * Populates a table with total project costs grouped by country.
 * <p>
 * Retrieves project cost data for each country where the logged-in user is a project manager. 
 * The data is displayed in a table with columns for the country name and total cost.
 * </p>
 *
 * @throws InfException if a database query error occurs.
 * @throws NumberFormatException if cost data cannot be parsed as a number.
 */
    public void fyllLandKostnadTabell() {
    try {
        
        String sql = "SELECT land.namn, SUM(projekt.kostnad) " +
                     "FROM projekt " +
                     "JOIN land ON projekt.land = land.lid " +
                     "WHERE projekt.projektchef IN (" +
                     "    SELECT aid FROM anstalld WHERE epost = '" + inloggadAnvandare + "'" +
                     ") " +
                     "GROUP BY land.namn";

        ArrayList<HashMap<String, String>> resultat = idb.fetchRows(sql);

        if (resultat == null || resultat.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingen statistik hittades.");
            return;
        }

        
        DefaultTableModel model = new DefaultTableModel(new String[]{"Land", "Total kostnad"}, 0);

        for (HashMap<String, String> rad : resultat) {
            
            String landNamn = rad.get("namn");
            double totalKostnad = Double.parseDouble(rad.get("SUM(projekt.kostnad)"));

            model.addRow(new Object[]{landNamn, totalKostnad});
        }

        
        tblLandKostnad.setModel(model);

    } catch (InfException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fel vid hämtning av statistik: " + e.getMessage());
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Felaktigt format för kostnad: " + e.getMessage());
    }
}

    
    
    
    
    
    
