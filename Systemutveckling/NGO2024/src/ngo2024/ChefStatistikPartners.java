    /**
 * Populates a table with the names of partners associated with the logged-in user's projects.
 * <p>
 * Retrieves a distinct list of partner names from the database for projects 
 * where the logged-in user is listed as the project manager. The results are displayed 
 * in a table with one column: "Partners".
 * </p>
 *
 * @throws InfException if a database query error occurs.
 */
    public void fyllStatistikTabell() {
        
        try {
            
            String sql = "SELECT DISTINCT namn " +
                     "FROM partner " +
                     "WHERE pid IN (" +
                     "    SELECT partner_pid " +
                     "    FROM projekt_partner " +
                     "    WHERE pid IN (" +
                     "        SELECT pid " +
                     "        FROM projekt " +
                     "        WHERE projektchef IN (" +
                     "            SELECT aid " +
                     "            FROM anstalld " +
                     "            WHERE epost = '" + inloggadAnvandare + "'" +
                     "        )" +
                     "    )" +
                     ");";
            
            ArrayList<HashMap<String, String>> statistikResultat = idb.fetchRows(sql);
            
            if (statistikResultat == null || statistikResultat.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog (this, "Ingen statistik hittades.");
                return;
            }
            
            
               DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Partners");
            
            
            for (HashMap<String, String> rad : statistikResultat) {
             model.addRow(new Object[]{rad.get("namn")});
            
            }
            
            
            tblStatistik.setModel(model);
            
            
            
            
        } catch (InfException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fel vid h√§mtning av statistik: " + e.getMessage());
        }
    }
    
    
    
    
    
    
    
    
    
    
