    //Metod för att hitta samtliga partners som hör till projektet.
    public void visapartnerinfofylltabell(){

     //Här hämtar vi information från databasen och gör en tabell. Sedan fyller vi värden i tabellen om data finns att hämta.   
     try{   
      String partnersql = "SELECT * FROM partner WHERE pid IN (SELECT partner_pid FROM projekt_partner WHERE pid IN (SELECT pid FROM ans_proj WHERE aid = '" + inloggadAnvandare + "'))";
            ArrayList<HashMap<String, String>> resultat = idb.fetchRows(partnersql);
            if (resultat == null || resultat.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Inga partners hittades.");
                return;
            }
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("namn");
            model.addColumn("kontaktperson");
            model.addColumn("kontaktepost");
            model.addColumn("telefon");
            model.addColumn("adress");
            model.addColumn("branch");
           
            
            
            for (HashMap<String, String> rad : resultat) {
                model.addRow(new Object[]{rad.get("namn"), rad.get("kontaktperson"), rad.get("kontaktepost"), rad.get("telefon"),
                rad.get("adress"), rad.get("branch")});
            }
            tblprojekt.setModel(model);
        } 
     //Visar eventuella fel
    catch (InfException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fel vid hämtning av partners: " + e.getMessage());
        }
    }
    

