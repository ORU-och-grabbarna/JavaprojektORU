    private void btnLäggTillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLäggTillActionPerformed
        // TODO add your handling code here:
        String hid = null;
        
        try{
            hid = idb.getAutoIncrement("hallbarhetsmal", "hid");
        } catch(InfException e){
            lblMeddelandeTop.setText("Problem med att autoinkrementera hid");
        }
        
        // få värden från textfields
        String namn = tfNamn.getText();
        String målnummer = tfMålnummer.getText();
        String prioritet = tfPrioritet.getText();
        String beskrivning = taBeskrivning.getText();
        
        // Validering
        
        // få ut alla målnummer
        ArrayList<String> målnummerLista = new ArrayList<String>();
        String getMålnumQuery = "SELECT malnummer FROM hallbarhetsmal";
        
        try{
            målnummerLista = idb.fetchColumn(getMålnumQuery);
        }catch(InfException e){
            javax.swing.JOptionPane.showMessageDialog (this, "Kunde inte hämta lista på alla målnummer.");
            return;
        }
        
        if(målnummerLista.contains(målnummer)){
            javax.swing.JOptionPane.showMessageDialog (this, "Målnumret finns redan i databasen. Försök med ett annat målnummer.");
            return;
        }
        
        if(!Validator.isValidName(namn)){
            javax.swing.JOptionPane.showMessageDialog (this, "hållbarhetsmålets namn kan endast bestå av bokstäver och mellanslag.");
            return;
        }
        if(!Validator.isPositiveNumber(målnummer)){
            javax.swing.JOptionPane.showMessageDialog (this, "Målnumret måste vara ett positivt nummer!");
            return;
        }
       
        // Inserta värden i databasen
        String InsertAvdelningQuery = "INSERT INTO hallbarhetsmal VALUES('" + hid + "','" +
                namn + "','" + målnummer + "','" + beskrivning + "','" + prioritet + "')";
        
        try{
            idb.insert(InsertAvdelningQuery);
            lblMeddelandeTop.setText("hållbarhetsmål tillagd.");
        }catch(InfException e){
            javax.swing.JOptionPane.showMessageDialog (this, "Kan inte lägga till hållbarhetsmålet, fel i query.");
        }
    }//GEN-LAST:event_btnLäggTillActionPerformed

    
