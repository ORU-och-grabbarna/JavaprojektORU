    private void btnHämtaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHämtaActionPerformed
        // TODO add your handling code here:
        // få ut info om hållbarhetsmål med namnet
     
        String InputHållbarhetsmålsnamn = tfInputHållbarhet.getText();
        
        String QueryHållbarhet = "SELECT * FROM hallbarhetsmal WHERE namn LIKE '%" + InputHållbarhetsmålsnamn + "%'";
        HashMap<String, String> HållbarhetsRow = new HashMap<String,String>();
        
        try {
            HållbarhetsRow = idb.fetchRow(QueryHållbarhet);
        } catch (InfException ex) {
            lblMeddelandeTop.setText("Problem med att hitta info om hållbarhetsmålet. Kontrollera att den existerar.");
        }
        
        // mata in värdena i textfields
        lblHid.setText(HållbarhetsRow.get("hid"));
        tfNamn.setText(HållbarhetsRow.get("namn"));
        tfMålnummer.setText(HållbarhetsRow.get("malnummer"));
        tfPrioritet.setText(HållbarhetsRow.get("prioritet"));
        taBeskrivning.setText(HållbarhetsRow.get("beskrivning"));
 
        
    }//GEN-LAST:event_btnHämtaActionPerformed

    private void btnÄndraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnÄndraActionPerformed
        // TODO add your handling code here:
        // ta in data från textfields
        String hid = lblHid.getText();
        String namn = tfNamn.getText();
        String målnummer = tfMålnummer.getText();
        String prioritet = tfPrioritet.getText();
        String beskrivning = taBeskrivning.getText();
        
         // Validering
        if(!Validator.isValidName(namn)){
            javax.swing.JOptionPane.showMessageDialog (this, "Målets namn kan endast bestå av bokstäver och mellanslag.");
            return;
        }
        if(!Validator.isPositiveNumber(målnummer)){
            javax.swing.JOptionPane.showMessageDialog (this, "Målnumret måste vara ett positivt nummer!");
            return;
        }
        
        
        // Uppdatera set where ändringarna
        String uppdateraHållbarhetsmål;
        
     
        uppdateraHållbarhetsmål = "UPDATE hallbarhetsmal SET namn = '" + namn + "', beskrivning = '" + beskrivning + "', malnummer = '"+ 
                målnummer + "', prioritet = '" + prioritet + "'" + "WHERE hid = '" + hid + "'" ;
        
        
        try{
            idb.update(uppdateraHållbarhetsmål);
            lblMeddelandeTop.setText("Hållbarhetsmålet har uppdaterats.");
        } catch (InfException e){
            lblMeddelandeTop.setText("Problem med att uppdatera hållbarhetsmålet i databasen.");
        }
        
       
    }//GEN-LAST:event_btnÄndraActionPerformed


    
    
