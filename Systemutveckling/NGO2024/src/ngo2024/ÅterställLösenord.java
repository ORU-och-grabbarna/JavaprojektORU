    private void btnÅterställActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnÅterställActionPerformed
        // TODO add your handling code here:
        
        // få info från textfields
        String epost = tfEpost.getText();
        String nyttLösen = tfNyttLösenord.getText();
        String uppLösen = tfUpprepa.getText();
        
        // validera värden inte är tomma
        if(epost.isEmpty() || nyttLösen.isEmpty() || uppLösen.isEmpty()){
            lblError.setText("Inga fält får vara tomma!");
            return;
        }
        // kontrolera att upprepa och nytt lösenord är lika
        if(!nyttLösen.equals(uppLösen)){
            lblError.setText("Upprepade lösenordet stämmer inte överens0");
            return;
        }
        // uppdatera databasen
        String updatePassQuery = "UPDATE anstalld SET losenord = '" + nyttLösen + "' WHERE epost = '" + epost + "'";
        
        try{
            idb.update(updatePassQuery);
            lblError.setText("Lösenordet har uppdaterats!");
        }catch(InfException e){
            javax.swing.JOptionPane.showMessageDialog(this, "Lösenordet kunde inte uppdateras. Kontrollera E-posten.");
        }
        
        
    }//GEN-LAST:event_btnÅterställActionPerformed

